package ex_4;

public class Dog {
    private String name;
    private int age, humanAge;

    public Dog (String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void humanAge (int age) {
        humanAge = age*7;
    }

    public int getHumanAge() {
        return humanAge;
    }

    @Override
    public String toString() {
        return "Dog{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}

